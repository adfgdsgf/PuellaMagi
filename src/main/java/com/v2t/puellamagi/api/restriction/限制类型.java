package com.v2t.puellamagi.api.restriction;

/**
 * 玩家行动限制类型
 *
 * 用于统一管理各种状态对玩家行动的限制
 * 如：假死、灵魂出窍、眩晕、沉默等
 */
public enum 限制类型 {

    /** 移动（WASD） */
    移动,

    /** 跳跃 */
    跳跃,

    /** 攻击实体 */
    攻击,

    /** 使用物品（右键物品本身，如吃东西、射箭） */
    使用物品,

    /** 释放技能 */
    释放技能,

    /** 交互方块（右键方块，如开箱子、按按钮） */
    交互方块,

    /** 交互实体（右键实体，如交易、骑乘） */
    交互实体,

    /** 丢弃物品 */
    丢弃物品,

    /** 破坏方块 */
    破坏方块
}
